#include "libft.h"

int	ft_putstr(char *s)
{
	int	i;
	int	z;

	i = 0;
	if (!s)
	{	
		write(1, "(null)", 6);
		return (6);
	}
	z = ft_strlen(s);
	while (s[i])
	{
		ft_putchar(s[i]);
					i++;
	}		
	return (z);
}
